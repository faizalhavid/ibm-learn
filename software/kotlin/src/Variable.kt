fun main() {
//    Immutable variable
    val a: Int = 10
    val b: Long = 20L
    val c: Short = 30
    val d: Byte = 40
    val e: Float = 50.0F
    val f: Double = 60.0
    f = 9
    print("test value $f")
//    Mutable variable
    var a1: Int = 10
    var b1: Long = 20L
    var c1: Short = 30
    var d1: Byte = 40
    var e1: Float = 50.0F
    var f1: Double = 60.0
// Nullable variable
    var a2: Int? = null
    var b2: Long? = null
    var c2: Short? = null
    var d2: Byte? = null
    var e2: Float? = null
    var f2: Double? = null
}